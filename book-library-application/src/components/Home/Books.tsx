interface Data {
  calories: number;
  carbs: number;
  fat: number;
  title: string;
  protein: number;
}

function createData(
  title: string,
  calories: number,
  fat: number,
  carbs: number,
  protein: number
): Data {
  return { title, calories, fat, carbs, protein };
}

